// ---- 反向合并：把扁平翻译表合并回 en.json 的结构 ----
function setDeep(obj, path, value) {
  const parts = path.split('.');
  let cur = obj;
  for (let i = 0; i < parts.length - 1; i++) {
    const k = parts[i];
    if (typeof cur[k] !== 'object' || cur[k] === null || Array.isArray(cur[k])) cur[k] = {};
    cur = cur[k];
  }
  cur[parts[parts.length - 1]] = value;
}

let written = 0, missing = 0;
const totalKeys = allKeys.length;
for (const [lang, flat] of Object.entries(LANGS)) {
  const out = JSON.parse(JSON.stringify(en)); // 深拷贝
  let langMissing = 0;
  for (const k of allKeys) {
    if (flat[k] != null) {
      setDeep(out, k, flat[k]);
    } else {
      langMissing++;
    }
  }
  // 占位符断言：含 {n}/{year} 的 key，翻译也必须保留占位符
  for (const k of allKeys) {
    const enVal = k.split('.').reduce((a, p) => (a == null ? a : a[p]), en);
    if (typeof enVal === 'string' && /\{[a-z]+\}/i.test(enVal)) {
      const trVal = k.split('.').reduce((a, p) => (a == null ? a : a[p]), out);
      if (typeof trVal === 'string' && !/\{[a-z]+\}/i.test(trVal)) {
        // 翻译后漏了占位符 → 报错
        throw new Error(`[${lang}] key "${k}" lost placeholder; en="${enVal}" tr="${trVal}"`);
      }
    }
  }
  fs.writeFileSync(path.join(ROOT, `${lang}.json`), JSON.stringify(out, null, 2));
  written++;
  missing += langMissing;
  console.log(`✓ ${lang.padEnd(6)} → ${langMissing === 0 ? '100%' : ((totalKeys - langMissing) + '/' + totalKeys)} (${totalKeys - langMissing}/${totalKeys})`);
}

console.log(`\nWrote ${written} dictionary files.`);
